export function add(a: number, b: number): number {
  return a + b;
}

export function subtract(a: number, b: number): number {
  return a - b;
}

export function add(a: string, b: string): string {
  return a + b;
}

export function subtract(a: string, b: string): string {
  return a.replace(b, "");
}
